# Задача 3: Конвертер температур
# Перевод температуры из Цельсия в Фаренгейты и определение состояния воды

# --- Исходные данные ---
celsius = 69.0  # Температура в градусах Цельсия

# --- Вычисления ---
fahrenheit = celsius * 9 / 5 + 32  # Формула перевода в Фаренгейты

# --- Определение состояния воды ---
if celsius <= 0:
    state = "Лёд"
elif celsius < 100:
    state = "Жидкость"
else:
    state = "Пар"

# --- Вывод ---
print("=== КОНВЕРТЕР ТЕМПЕРАТУР ===")
print(f"Температура: {celsius} °C")
print(f"По Фаренгейту: {fahrenheit:.2f} °F")
print(f"Состояние воды: {state}")