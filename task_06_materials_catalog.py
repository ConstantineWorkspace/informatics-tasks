# Задача 6: Каталог материалов
# Работа со списками — индексация, срезы, добавление, удаление элементов

# --- Исходный список из 5 материалов ---
materials = ["Штукатурка", "Плитка", "Брус", "Краска", "Шлак"]

# --- Вывод элементов ---
print("=== КАТАЛОГ МАТЕРИАЛОВ ===")
print(f"Первый материал: {materials[0]}")
print(f"Последний материал: {materials[-1]}")
print(f"Средние элементы: {materials[1:-1]}")

# --- Добавляем 2 новых материала ---
materials.append("Гранит")
materials.append("Базальт")
print(f"\nПосле добавления: {materials}")

# --- Удаляем второй элемент ---
materials.pop(1)
print(f"После удаления второго элемента: {materials}")

# --- Итоговый список ---
print(f"\nИтоговый список: {materials}")
print(f"Количество материалов: {len(materials)}")